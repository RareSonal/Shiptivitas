    id SERIAL PRIMARY KEY,
    user_id INT,
    login_timestamp INT
);

-- Table: users
CREATE TABLE users (
    id SERIAL PRIMARY KEY,
    firstname VARCHAR(255),
    lastname VARCHAR(255),
    pin VARCHAR(4)
);


